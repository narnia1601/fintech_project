<script>
  import '../assets/js/core/popper.min.js'
  import '../assets/js/core/bootstrap.min.js'
  import readXlsxFile from 'read-excel-file'
  export default {
    data(){
      return {
        financialData: {}
      }
    },
    methods: {
      selectFile(e){
        let file = e.target.files ? e.target.files[0] : null
        this.selectedFile = file
        readXlsxFile(file).then(rows => {
          this.financialData.title = rows[1][0]
          // current ratio
          this.financialData.currentAssets = rows[9][1]
          this.financialData.currentLiabilities = rows[21][1]
          // quick ratio
          this.financialData.cash = rows[4][1]
          this.financialData.accountsReceivable = rows[5][1]
          // leverage ratio
          this.financialData.equity = rows[25][1] - rows[21][1]
          // debt to equity ratio
          this.financialData.assets = rows[14][1]
          // ebit
          this.financialData.ebit = rows[45][1] + rows[44][1] + rows[42][1]
          // interest expense
          this.financialData.interestExpense = rows[42][1]
          // net income
          this.financialData.netIncome = rows[45][1]
        })
      },
      submitFile(){
        this.$router.push({
          path: '/risk',
          query: {
            title: this.financialData.title,
            currentAssets: this.financialData.currentAssets,
            currentLiabilities: this.financialData.currentLiabilities,
            cash: this.financialData.cash,
            accountsReceivable: this.financialData.accountsReceivable,
            equity: this.financialData.equity,
            assets: this.financialData.assets,
            ebit: this.financialData.ebit,
            interestExpense: this.financialData.interestExpense,
            netIncome: this.financialData.netIncome
          }
        })
      }
    }
  }
</script>

